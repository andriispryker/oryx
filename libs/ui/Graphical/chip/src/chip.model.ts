export enum ChipType {
  DEFAULT = 'default',
  SUCCESS = 'success',
  OFFLINE = 'offline',
  INACTIVE = 'inactive',
  LOW = 'low',
  INFO = 'info',
  WARNING = 'warning',
  ERROR = 'error',
  /**
   * `ONLINE` is a synonym for the `SUCCESS` chip type.
   */
  ONLINE = 'online',
  /**
   * `ACTIVE` is a synonym for the `SUCCESS` chip type.
   */
  ACTIVE = 'active',
}
