export interface SwatchAttributes {
  /**
   * Defines the color that is used as-is as the swatch colors. Any html color can be used, from any
   * color supported color systems that are provided by the web platform. For example web colors (red, blue, rebeccapurple), hex colors (#ff3300), rgba(255,100, 100, 0.5), etc.
   * An image can also be applied for the swatch.
   */
  color?: string;
}
